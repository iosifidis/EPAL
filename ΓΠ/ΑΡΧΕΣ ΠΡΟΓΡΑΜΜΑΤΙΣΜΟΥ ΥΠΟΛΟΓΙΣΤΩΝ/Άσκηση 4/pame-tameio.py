# Υπολογισμοί ποσού πληρωμής και συνολικού ποσού με ΦΠΑ
fpa=24/100 # Εισάγουμε μεταβλητή για το ΦΠΑ γιατί εάν αλλάξει στο μέλλον, το αλλάζουμε εδώ.
