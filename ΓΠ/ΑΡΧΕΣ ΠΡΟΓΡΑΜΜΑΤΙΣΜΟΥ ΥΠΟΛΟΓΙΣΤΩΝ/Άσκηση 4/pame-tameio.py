# Διαβάζει τιμές και ποσότητα προϊόντος
posotita=float(input('Δώστε την ποσότητα που πήρε ο πελάτης (κιλά ή τεμάχια):')) # Μπορεί να είναι και δεκαδικό (πχ κιλά)
